    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         username=jTextField1.getText();
        char pass[]=jPasswordField1.getPassword();
        String password=String.valueOf(pass);
        
        if(username.isEmpty())
            JOptionPane.showMessageDialog(null,"Please Enter Username");
        else if(password.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Enter Passsword");
        else
        {
            Login li=new Login();
            if(li.doLogin(username, password))
            {
                EditProfileFrame.username=username;
                this.dispose();
                OperationFrame of=new OperationFrame();
                of.setVisible(true);
                of.setSize(950,600);
            }
            else
                JOptionPane.showMessageDialog(null, "Please Enter Correct Username or PAssword");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

